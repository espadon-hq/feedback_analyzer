"""Модуль імпорту відгуків з CSV файлів різних форматів."""

import pandas as pd


def get_columns(filepath: str) -> list[str]:
    """Повертає список назв колонок CSV файлу без завантаження даних.

    Читає лише заголовок файлу (nrows=0), що дозволяє швидко отримати
    структуру навіть великого файлу без витрат пам'яті.

    Args:
        filepath: Шлях до CSV файлу на диску.

    Returns:
        Список рядків з назвами колонок у порядку їх появи у файлі.

    Raises:
        FileNotFoundError: Якщо файл не знайдено за вказаним шляхом.
        pandas.errors.EmptyDataError: Якщо файл порожній.
    """
    # nrows=0 — завантажуємо лише рядок заголовка, без жодного рядка даних
    df = pd.read_csv(filepath, nrows=0)
    return df.columns.tolist()


def load_texts(filepath: str, column: str) -> list[str]:
    """Завантажує та очищає текстові відгуки з вказаної колонки CSV.

    Підтримує різні формати файлів — власний, Amazon, Trustpilot тощо.
    Головна умова: файл повинен містити колонку з назвою, переданою в column.

    Правила очищення:
        - Пусті значення (NaN) — пропускаються
        - Рядки коротші за 3 символи — пропускаються (занадто короткі для аналізу)
        - Пробіли на початку та в кінці рядка — видаляються

    Args:
        filepath: Шлях до CSV файлу на диску.
        column: Назва колонки, яка містить текст відгуку.
                Наприклад: 'text', 'review_body', 'Review Text'.

    Returns:
        Список очищених непорожніх рядків, готових до аналізу тональності.

    Raises:
        ValueError: Якщо вказана колонка відсутня у файлі.
    """
    # Перевіряємо чи існує вказана колонка у файлі
    available_columns = get_columns(filepath)
    if column not in available_columns:
        raise ValueError(
            f"Колонка '{column}' не знайдена у файлі. "
            f"Доступні колонки: {available_columns}"
        )

    # Завантажуємо лише потрібну колонку — економимо пам'ять при великих файлах
    df = pd.read_csv(filepath, usecols=[column])

    # Ланцюжок очищення:
    # 1. dropna() — прибираємо порожні значення (NaN)
    # 2. astype(str) — конвертуємо все в рядки (на випадок числових значень)
    # 3. str.strip() — видаляємо зайві пробіли
    texts = (
        df[column]
        .dropna()
        .astype(str)
        .str.strip()
    )

    # Фільтруємо рядки коротші за 3 символи —
    # такі тексти не несуть корисної інформації для аналізу тональності
    return [t for t in texts if len(t) >= 3]